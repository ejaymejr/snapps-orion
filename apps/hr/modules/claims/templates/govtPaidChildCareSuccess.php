		<td class="span4 bg-clearBlue alignRight">What is the Government-Paid <br>Childcare Leave (GPCL)?</td>
		<td class="span8 " >
Working parents with at least one child who is a Singapore Citizen <b class="text-warning">under the age
<br />of 7 years</b> are entitled to 6 days of paid child care leave every year. The first 3
<br />days are employer-paid, and the last <b class="text-info">3 days</b> are Government-paid (capped at
<br />$500 per day, inclusive of CPF contributions).
<br /><br />
From 1 May 2013, working parents with the youngest child who is aged between
<br /><b class="text-warning">7 and 12 years</b> inclusive are entitled to <b class="text-info">2 days</b> of government-paid child care
<br />leave a year. The Government will reimburse the employer or self-employed
<br />parent the 2 days of extended child care leave a year (capped at $500 per day,
<br />inclusive of CPF contributions).
<br /><br />
<br />For more information on the benefits under GPCL scheme, 
<br />please visit <a href="http://www.ecitizen.gov.sg/Topics/Pages/Pro-Family-Leave-Schemes-available.aspx" target="_BLANK">Pro-family Leave website</a>.
		
